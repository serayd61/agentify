// Helper functions
export function getAgentBySlug(slug: string): AgentTemplate | undefined {
  return agentTemplates.find((agent) => agent.slug === slug);
}

export function getAgentsByCategory(categorySlug: string): AgentTemplate[] {
  return agentTemplates.filter((agent) => agent.categorySlug === categorySlug);
}

export function getPopularAgents(): AgentTemplate[] {
  return agentTemplates.filter((agent) => agent.isPopular);
}

export function getNewAgents(): AgentTemplate[] {
  return agentTemplates.filter((agent) => agent.isNew);
}

export function getCategoryBySlug(slug: string): AgentCategory | undefined {
  return categories.find((cat) => cat.slug === slug);
}

export function searchAgents(query: string): AgentTemplate[] {
  const lowerQuery = query.toLowerCase();
  return agentTemplates.filter(
    (agent) =>
      agent.name.toLowerCase().includes(lowerQuery) ||
      agent.description.toLowerCase().includes(lowerQuery) ||
      agent.category.toLowerCase().includes(lowerQuery)
  );
}

